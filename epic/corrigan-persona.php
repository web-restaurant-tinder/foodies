	<ul>
		<li>Name: Jeremy</li>
		<li>Age:24</li>
		<li>Technology: Jeremy is young and very tech savvy. He uses all Apple products. He has an iPhone, iPad, Apple Watch, and a Mac Computer. All of these technologies are always using the latest software updates. </li>
		<li>Profession: Working as a Graduate Assistant at a local University where he is working on his Master's in Philosophy and teaching philosophy to undergrads. </li>
		<li>Jeremy uses his computer, phone, and tablet everyday. He will search the web to find fun things to do on the weekend as well as pull up Reddit to find answers to many of his questions. He will also watch YouTube videos to learn how to do new things.</li>
		<li>Jeremy has frustrations in not being able to quickly decide where he and a friend will go out to eat, he also has a difficult time on finding new restaurants that he would like to try in his area. </li>
	</ul>

<h3>User Story</h3>
	<p>As a monthly user of Restaurant Tinder, Jeremy wants to be able to quickly and easily find somewhere to eat that he and friends will enjoy.</p>

<h3>Use Case</h3>
	<p>It is 7pm. Jeremy and his friend has listed dozens of possible places to go out to eat together and they have yet to come to an agreement. Jeremy remembers Restaurant Tinder. He then grabs his iPhone 10X and goes to the Restaurant Tinder app using the Safari browser.
	He logs in and so does his friend. They begin swiping left or right to indicate a restaurant they like or do not like. After a few minutes of both of them swiping they realize they have matched on a number of restaurants. They finally pick a new restaurant to try out that they
	both like.</p>

<h3>Interaction Flow</h3>
	<ul>
		<li>Jeremy pulls out his iPhone 10X which is using the latest iOS software update and navigates to the Safari browser. He then types in the Restaurant Tinder web address in the search bar.</li>
		<li>The browser then opens up a set of results for the search.</li>
		<li>Jeremy will then click the Restaurant Tinder link, which will bring him to the homepage of Restaurant Tinder.</li>
		<li>Jeremy will then be brought to a login screen, Jeremy will put in his Username and Password and click the submit button.</li>
		<li>A new screen will appear and it will be a screen that has a restaurant and two buttons on the bottom, Jeremy can click one of the two buttons on the bottom to indicate if he likes that restaurant, he can also swipe the picture left or right.</li>
		<li>Jeremy will then begin clicking on the like button (or swipe right) or the no thanks (swipe left) button depending on his preferences.</li>
		<li>After a few minutes of swiping, Jeremy will then go to the search bar and type in his friends name and click submit.</li>
		<li>A new screen will appear which will list all the people with that name. Jeremy will scroll to his friend and click his name.</li>
		<li>A new screen will appear which shows information about that particular user, as well as have a follow button under his name. Jeremy will hit follow.</li>
		<li>Once the two are following each other then there will be a button on the page that reads "Restaurants in common," they will click that button.</li>
		<li>A new screen will appear that shows all of the restaurants that the two have in common.</li>
		<li>The two will look at the list of restaurants and make a decision as to where to eat.</li>
	</ul>

